    <div class="row row-art">
        <h2 class="text-center col-12">Mon Stage</h2>
        <p class="col-12">Durant ces 6 semaines il m’a été demandé de créer un site afin de gérer l’activité de cette association. </p>
        <ul>
            <li>L’association étant dépourvue de tout expert technique il m’a fallut agir avec organisation et autonomie.</li>
            <li>Les objectifs étaient multiples la solution devait répondre à nombres de critère.</li>
            <li>Le site devait pouvoir faire la gestion et l’archivage des commandes.</li>
            <li>De ce fait un client devait pouvoir grâce au site créer son panier d’association produit, visuel et générer un bon de commande.</li>
            <li>Les élèves quant à eux devaient pouvoir saisir les bons de commandes que les autres élèves de l’établissement apportaient.</li>
            <li>Un portail administratif à été créé pour gérer l’archivage des commandes et permettre la gestion de leur statut.</li>
            <li>Pour chaque commande l’administrateur peut générer la facture autant de fois qu’il le souhaite.</li>
            <li>Le site devait effectuer la gestion des visuels</li>
            <li>Cette gestion s’est accompagnée de la création d’un catalogue permettant de visualiser les visuels disponibles.</li>
            <li>Pour chaque commandes il est possible de générer un PDF qui contient les différents visuels sélectionnés dans la commande.</li>
            <li>L’administrateur peut rajouter des visuels par famille dans la base de donnée.</li>
            <li>Un objectif majeur du site est d’être extrêmement visuel et donc facile à utiliser; de ce fait les différents liens du menu sont des icônes, 
                                      les boutons de navigation sont très colorés, et le tableau d’administration fonctionne grâce à un système de couleurs: 
                                      le bouton vert renvoi au tableau vert, etc. </li>
        </ul>

