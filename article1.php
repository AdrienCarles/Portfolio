<div class="container-fluid cv card">
    <div class="row row-art">
        <p>Les technologies de l’information et de la communication (TIC) représentent pour les personnes handicapées, quelque soit le handicap, une réelle opportunité pour leur intégration, aussi bien dans la vie quotidienne que professionnelle, sous réserve que leur accessibilité soit prise en compte. Malheureusement même si ces personnes représentent plus de 10 % de la population, l’accessibilité des TIC ne fait pas partie des priorités des fournisseurs ou développeurs impliqués dans les TIC ni pour les concepteurs de site internet.</p>
        <h4 class="text-center">En quoi consiste une aide technique pour handicapés ?</h4>
        <p>L’aide technique va permettre à une personne handicapée d’utiliser les logiciels installés sur son micro-ordinateur ou son Smartphone et dépend du type d’handicap. L’utilisation d’un micro-ordinateur et d’un Smartphone impose le bon fonctionnement de certains de nos sens et de nos capacités physiques. L’aide technique met en place une alternative pour pallier le handicap. L’usage du micro-ordinateur par les aveugles est probablement le cas le plus compliqué à résoudre : la figure suivante montre les adaptations permettant de rendre un ordinateur accessible.</p>
        <img src="IMG/article1-illustration1.png" alt="Illustration">
    </div>
    <div class="row row-art">
        <h4>L’environnement technique numérique accessible</h4>
    </div> 
    <div class="row">  
        <p>Pour l’accessibilité des TIC, il est indispensable de prévoir :</p>
        <ul>
            <li>Des alternatives au clavier et à la souris, qui sont obligatoires pour les déficients visuels et les handicapés moteurs.</li>
            <li>Le respect du standard d’interface du système d’exploitation, par exemple Windows le standard MSAA (Microsoft Active Accessibility) pour l’interopérabilité avec les aides techniques utilisées par les handicapés.</li>
            <li>La présentation des informations à l’écran en respectant les règles « WCAG 2.0 » du World Wide Web Consortium (W3C).</li>
        </ul>
    </div>
    <div class="row row-art"> 
        <h4>Ce qu’il faudrait faire</h4>
        <p>Développer une solution accessible ne coûte pas plus cher si l’accessibilité est prise en compte dès la première phase de spécification et de conception des applications, des sites internet, des systèmes, des produits et des services. La méconnaissance des règles à respecter n’est pas suffisante pour justifier une aussi faible prise en compte de l’accessibilité. Certains, ne voyant jamais de personnes handicapées, ne comprennent pas l’utilité de ces règles. Pourtant, si l’accessibilité était prise en compte les handicapés seraient plus présents. La solution la plus efficace consisterait sans doute à mettre l’accessibilité comme une norme obligatoire sans discuter le pourcentage d’utilisateurs potentiels…</p>
    </div>

